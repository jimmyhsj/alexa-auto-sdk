package com.amazon.alexa.auto.setup.workflow.fragment;

import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.fragment.app.Fragment;

import com.amazon.alexa.auto.setup.R;

/**
 * Fragment for preventing vehicles from continuing setup flow while driving.
 */
public class DriveBlockSetupFragment extends Fragment {
    private static final String TAG = DriveBlockSetupFragment.class.getSimpleName();

    /**
     * Constructs an instance of DriveBlockSetupFragment
     */
    public DriveBlockSetupFragment() {}

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState) {
        return inflater.inflate(R.layout.drive_block_setup_fragment, container, false);
    }

    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
    }

    @Override
    public void onActivityCreated(Bundle savedInstanceState) {
        super.onActivityCreated(savedInstanceState);

        View fragmentView = requireView();
        TextView backButtonText = fragmentView.findViewById(R.id.block_setup_on_drive_back_btn);
        if (getActivity() != null)
            backButtonText.setOnClickListener(view -> getActivity().onBackPressed());
    }
}